import kotlin.math.*
fun main()
{
    println(max( 5, 10))           //O maior número entre dois valores
    println(min(5, 10))            //O menor número entre dois valores //
    println(sqrt(45.67))              //Saber a raiz quadrada
    println(PI)                          //Valor de PI
    println(E)                           //Valor de Euler
    println(round(435.42342343))      //Arredonda valores "quebrados" para inteiro
}